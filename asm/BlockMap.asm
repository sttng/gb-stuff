; ==========================================================================
; ldir / Memcopy
; --------------------------------------------------------------------------
; Copies specified memory-location to another location. 'Fake' ldir from Z80
; --------------------------------------------------------------------------
; Parameters:
; HL: Source
; DE: Destination
; BC: Count / Size
; ==========================================================================
ldir:
	ld a,[hl+]
	ld [de],a
	inc de
	dec bc
	ld a,b
	or c
	jr nz,ldir
	ret

